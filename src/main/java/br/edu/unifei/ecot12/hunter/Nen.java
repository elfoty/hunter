package br.edu.unifei.ecot12.hunter;


public class Nen {
	private boolean situacaoAura;
	private String tipoNen;
	private int ren;
	private int ten;
	private int zetsu;
	private int hatsu;
	public boolean isSituacaoAura() {
		return situacaoAura;
	}
	public void setSituacaoAura(boolean situacaoAura) {
		this.situacaoAura = situacaoAura;
	}
	public String getTipoNen() {
		return tipoNen;
	}
	public void setTipoNen(String tipoNen) {
		this.tipoNen = tipoNen;
	}
	public int getRen() {
		return ren;
	}
	public void setRen(int ren) {
		this.ren = ren;
	}
	public int getTen() {
		return ten;
	}
	public void setTen(int ten) {
		this.ten = ten;
	}
	public int getZetsu() {
		return zetsu;
	}
	public void setZetsu(int zetsu) {
		this.zetsu = zetsu;
	}
	public int getHatsu() {
		return hatsu;
	}
	public void setHatsu(int hatsu) {
		this.hatsu = hatsu;
	}
	
}
